#include "ft_printf.h"

int	ft_printf(const char *format, ...)
{
	va_list	args;
	int		i;
	int		count;
	int		ret;

	va_start(args, format);
	i = 0;
	count = 0;
	while (format[i])
	{
		if (format[i] == '%')
		{
			i++;
			ret += ft_format_handler(format[i], args);
			if (ret == -1)
				return (-1);
			count += ret;
		}
		else
		{
			write(1, &format[i], 1);
			count++;
		}
		i++;
	}
	va_end(args);
	return (count);
}

int	main (void)
{
	ft_printf("Score: %d | Index: %i\n", 42, -7);
	return (0);
}
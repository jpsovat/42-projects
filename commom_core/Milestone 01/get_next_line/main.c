#include "get_next_line.h"
#include <fcntl.h>
#include <stdio.h>
#include <unistd.h>

/* ========================================================================== */
/*                                 MAIN TEST                                  */
/* ========================================================================== */

int	main(void)
{
	char	*line;
	int		fd;

	fd = open("archive.txt", O_RDONLY);
	if (fd < 0)
		return (1);
	while ((line = get_next_line(fd)) != NULL)
	{
		printf("%s", line);
		free(line);
	}
	close(fd);
	return (0);
}
